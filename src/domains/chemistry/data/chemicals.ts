export const CHEMICAL_BY_ID: Record<string, Chemical> = Object.fromEntries(
  CHEMICALS.map((c) => [c.id, c]),
);

export function getChemical(id: string): Chemical | undefined {
  return CHEMICAL_BY_ID[id];
}

export function findChemicalByFormula(formula: string): Chemical | undefined {
  return CHEMICALS.find((c) => c.formula === formula);
}

export function findOrCreateProduct(
  formula: string,
  defaults: Partial<Chemical> & Pick<Chemical, "name" | "category" | "state">,
): Chemical {
  const existing = findChemicalByFormula(formula);
  if (existing) return existing;
  return chem({
    id: `gen-${formula.toLowerCase().replace(/[^a-z0-9]/gi, "")}`,
    formula,
    icon: "⚗️",
    color: defaults.color ?? "#e0e0e0",
    solubility: defaults.solubility ?? "n/a",
    hazardLevel: defaults.hazardLevel ?? 1,
    subcategory: defaults.subcategory ?? "product",
    acidBase: defaults.acidBase ?? "none",
    ions: defaults.ions,
    cation: defaults.cation,
    anion: defaults.anion,
    ...defaults,
  });
}

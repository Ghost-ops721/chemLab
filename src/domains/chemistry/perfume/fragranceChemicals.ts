function chem(
  partial: Omit<Chemical, "domain" | "tags"> & { tags?: string[] },
): Chemical {
  return {
    domain: "chemistry",
    tags: partial.tags ?? [partial.category, partial.subcategory],
    ...partial,
  };
}

